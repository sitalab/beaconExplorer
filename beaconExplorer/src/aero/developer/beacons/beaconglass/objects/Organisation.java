package aero.developer.beacons.beaconglass.objects;

import java.io.Serializable;

public class Organisation implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 3985103737064681642L;
	private String idOrganisation = null;
	private String systemName = null;
	private String uuid = null;
	private String defaultLocation = null;
	private String name = null;

	public Organisation() {
	}

	public Organisation(String idOrganisation, String systemName, String uuid,
			String defaultLocation, String name) {
		this.idOrganisation = idOrganisation;
		this.systemName = systemName;
		this.uuid = uuid;
		this.defaultLocation = defaultLocation;
		this.name = name;
	}

	public String getIdOrganisation() {
		return idOrganisation;
	}

	public void setIdOrganisation(String idOrganisation) {
		this.idOrganisation = idOrganisation;
	}

	public String getSystemName() {
		return systemName;
	}

	public void setSystemName(String systemName) {
		this.systemName = systemName;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getDefaultLocation() {
		return defaultLocation;
	}

	public void setDefaultLocation(String defaultLocation) {
		this.defaultLocation = defaultLocation;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Organisation [idOrganisation=" + idOrganisation
				+ ", systemName=" + systemName + ", uuid=" + uuid
				+ ", defaultLocation=" + defaultLocation + ", name=" + name
				+ "]";
	}

}
